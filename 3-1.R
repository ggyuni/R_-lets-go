b <- 20 ## b에 20을 입력함

c <- a + b ## C에 a 더하기 b를 입력함

print(a) ## a의 값을 도출 (10이 도출됨)

print(b) ## b의 값이 도출 (20이 도출됨)

print(c) ## a더하기b의 값이 도출 (10+20=30이 도출)


